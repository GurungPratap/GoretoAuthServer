    //Alternative 2
    //this is useful when you want to use public key private key
    //and resource server can validate token by itself with public key
    @Bean
    public JWKSource<SecurityContext> jwkSource() throws Exception {
        KeyStore keyStore = KeyStore.getInstance("JKS");
        try (InputStream inputStream = new ClassPathResource(jwtPrivateKey).getInputStream()) {
            keyStore.load(inputStream, jwtPrivateKeyStorePassword.toCharArray());
        }

        // Get the private key and cert
        RSAPrivateKey privateKey = (RSAPrivateKey) keyStore.getKey(jwtPrivateKeyAlias, jwtPrivateKeyPwd.toCharArray());
        java.security.cert.Certificate cert = keyStore.getCertificate(jwtPrivateKeyAlias);
        RSAPublicKey publicKey = (RSAPublicKey) cert.getPublicKey();

        RSAKey rsaKey = new RSAKey.Builder(publicKey)
                .privateKey(privateKey)
                .keyID("auth-server-key") // This ID can be used in JWKS
                .build();

        JWKSet jwkSet = new JWKSet(rsaKey);
        return (selector, context) -> selector.select(jwkSet);

    }

